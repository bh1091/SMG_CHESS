package org.chenji.hw7;
public class ChessMessages {
String userTurn(String color) {
  return color + ": Your Turn";
}
String opponentTurn(String color, String player) {
  return color + ": " + player + "'s Turn";
}
String startTime(String time) {
  return "Start Time: " + time;
}
String newItem(String time, String opponent, String turn, String id) {
  return "Time: " + time + "; Opponent: " + opponent + "; Turn: " + turn + "; MatchId: " + id;
}
String win(String matchId) {
  return "Congraduations, you win in game " + matchId;
}
String lose(String matchId, String opponent) {
  return "Sorry, you lose to " + opponent + " in game " + matchId;
}
String draw(String matchId) {
  return "you draw in game " + matchId;
}
}
